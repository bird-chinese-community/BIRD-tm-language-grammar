const missing = [];

for (const check of checks) {
  for (const token of check.tokens) {
    const isScopeName = token.endsWith(".bird");
    const covered = isScopeName
      ? scopeNames.has(token)
      : literalWords.has(token) || matchesFullPhrase(semanticPatterns, token);

    if (!covered) {
      missing.push(`${check.label}: ${token}`);
    }
  }
}

const phraseChecks = [
  {
    label: "RADV sensitive route lifetime phrase",
    input: "route lifetime 1800 sensitive yes;",
    expected: ["route lifetime", "sensitive"],
  },
  {
    label: "RADV sensitive valid lifetime phrase",
    input: "valid lifetime 3600 sensitive yes;",
    expected: ["valid lifetime", "sensitive"],
  },
  {
    label: "RADV sensitive preferred lifetime phrase",
    input: "preferred lifetime 1800 sensitive yes;",
    expected: ["preferred lifetime", "sensitive"],
  },
];

for (const check of phraseChecks) {
  const matches = [...check.input.matchAll(radvPhraseRegex)].map((match) =>
    match[0].toLowerCase(),
  );

  for (const expected of check.expected) {
    if (!matches.includes(expected)) {
      missing.push(`${check.label}: ${expected}`);
    }
  }
}

const bitwisePattern = semanticPatterns.find(
  (pattern) => pattern.name === "keyword.operator.bitwise.bird",
);
const operatorChecks = [
  { input: "&", expected: true },
  { input: "|", expected: true },
  { input: "&&", expected: false },
  { input: "||", expected: false },
];

if (!bitwisePattern) {
  missing.push("BIRD 3.3 bitwise operators: pattern missing");
} else {
  for (const check of operatorChecks) {
    if (bitwisePattern.regex.test(check.input) !== check.expected) {
      missing.push(`BIRD 3.3 bitwise operators: ${check.input}`);
    }
  }
}

if (missing.length > 0) {
  console.error("Missing grammar coverage:");
  for (const item of missing) {
    console.error(`- ${item}`);
  }
  process.exit(1);
}

console.log(
  `Grammar coverage checks passed (${checks.reduce((sum, check) => sum + check.tokens.length, 0)} tokens, ${phraseChecks.length} phrase checks, ${operatorChecks.length} operator checks).`,
);
